          <li><h4>EZ Parking: Smart Parking Space Reservation Using Internet of Things - IEEE Conference Publication</h4> </li>
          </ul> 
         <p>Due to massive urbanization the demands of parking spaces around the city increase rapidly. The aim of this study was to find parking spaces and monitor their usage from anywhere in the world. The objective was to allow general people with vehicles find parking spaces around the city and allow parking owners to rent out their parking spaces. A mobile application searches for parking spaces using GPS and a marker shows the exact location of the parking lot. The driver then travels to the parking lot and parks their vehicle. The garages are set up using custom-made pressure sensors. Once the car parks in the allotted space, the sensors activate and sends signals to the mobile application confirming the entry of the vehicle. It then automatically starts a timer, which then notifies the space owner about the time of entry of the vehicle. As the reservation ends, a bill is auto-generated showing the total cost of usage of the parking space. It saves time for both the drivers' and the space providers as the sensors are highly responsive and the process is fully automatic.</p>
