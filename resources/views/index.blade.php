          <p>Poor road conditions are one of the major causes for road accidents. Developing countries in particular are witnessing increased accident rates due to these poor road conditions. Potholes, deep ridges, missing pitches, improper speed breakers, poorly constructed manhole covers and slabs all combine to greatly increase the probability of serious accidents thus transforming roads into obstacle courses. In this study we have developed a model to detect unwanted potholes, deep ridges and speed breakers using computer vision and machine learning tools. We have developed a customized dataset (called Bumpy) that we use to train our machine learning algorithms. In this paper we propose a method where we use the Tensorflow pre-trained model to detect the potholes, deep ridges and speed breakers. Our experimental results demonstrate high accuracy although there are many obstacles on the road.</p>
